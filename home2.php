				<p>The people of Bangladesh discovered their identity through the Language Movement in 1952. The struggle to establish their identity and national spirit began soon after 1947, when the British left India dividing it into two countries: India and Pakistan. Bangladesh, then East Pakistan, was part of Pakistan, which was put together by combining two geographically, culturally, and linguistically separate groups of people. The people of Bangladesh soon realized that being a part of Pakistan, which was created on the two nation theory, there was little scope for the distance culture of Bangladeshis to flourish. The Bangla language was the most important vehicle of the cultural expression for the people of this land. The refusal of the central government in West Pakistan to grant official status to the Bangla became the focal point of struggle.

